function F = t5_calc_cheb_coefs(Y_0, Y_end, t_0, t_end)
% многочлены Чебышева 1-го рода
x = t_0;
A1 = [1 x (2*x^2 - 1) (4*x^3 - 3*x) (8*x^4 - 8*x^2 + 1) (16*x^5 - 20*x^3 + 5*x);
     0 1  4*x        (12*x^2 - 3)  (32*x^3 - 16*x)     (80*x^4 - 60*x^2 + 5);
     0 0  4           24*x         (96*x^2 - 16)       (320*x^3 - 120*x)];
x = t_end;
A2 = [1 x (2*x^2 - 1) (4*x^3 - 3*x) (8*x^4 - 8*x^2 + 1) (16*x^5 - 20*x^3 + 5*x);
     0 1  4*x        (12*x^2 - 3)  (32*x^3 - 16*x)     (80*x^4 - 60*x^2 + 5);
     0 0  4           24*x         (96*x^2 - 16)       (320*x^3 - 120*x)];
A = [A1; A2];
B = [Y_0(1);
     Y_0(2);
     Y_0(3);
     Y_end(1);
     Y_end(2);
     Y_end(3)];
F = inv(A)*B;