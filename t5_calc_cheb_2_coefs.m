function F = t5_calc_cheb_2_coefs(Y_0, Y_end, t_0, t_end)
% многочлены Чебышева 2-го рода
x = t_0;
A1 = [1 2*x (4*x^2 - 1) (8*x^3 - 4*x) (16*x^4 - 12*x^2 + 1) (32*x^5 - 32*x^3 + 6*x);
     0  2    8*x        (24*x^2 - 4)  (64*x^3 - 24*x)       (160*x^4 - 96*x^2 + 6);
     0  0    8           48*x         (192*x^2 - 24)        (640*x^3 - 192*x)];
x = t_end;
A2 = [1 2*x (4*x^2 - 1) (8*x^3 - 4*x) (16*x^4 - 12*x^2 + 1) (32*x^5 - 32*x^3 + 6*x);
     0  2    8*x        (24*x^2 - 4)  (64*x^3 - 24*x)       (160*x^4 - 96*x^2 + 6);
     0  0    8           48*x         (192*x^2 - 24)        (640*x^3 - 192*x)];
A = [A1; A2];
B = [Y_0(1);
     Y_0(2);
     Y_0(3);
     Y_end(1);
     Y_end(2);
     Y_end(3)];
F = inv(A)*B;